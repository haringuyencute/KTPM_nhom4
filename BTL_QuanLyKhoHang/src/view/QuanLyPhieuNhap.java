    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        // TODO add your handling code here:
        String maTimKiem = txtIDTimKiem.getText();

    if (maTimKiem.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập mã phiếu nhập để tìm kiếm", "Thông báo", JOptionPane.WARNING_MESSAGE);
        return;
    }

    List<PhieuNhap> list = controller.readDataFromFile("src/file/pnhap.txt");
    DefaultTableModel model = (DefaultTableModel) tblPhieuNhap.getModel();
    model.setRowCount(0); // Clear the table before populating with search results

    boolean found = false;

    for (PhieuNhap pn : list) {
        if (pn.getMaNhap().equals(maTimKiem)) {
            model.addRow(new Object[]{pn.getMaNhap(), pn.getNgayNhap(), pn.getSoLuongNhap(), pn.getGiaNhap(), pn.getTenNCC(), pn.getTenNV()});
            found = true;
            break; // Stop searching once found
        }
    }

    if (!found) {
        JOptionPane.showMessageDialog(this, "Không tìm thấy phiếu nhập có mã " + maTimKiem, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
    }
       // JOptionPane.showMessageDialog(this, "Không có dữ liệu để tìm kiếm", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnTimActionPerformed

