    private void sortPhieuNhapByMaNhap(List<PhieuNhap> list) {
        list.sort(new Comparator<PhieuNhap>() {
            @Override
            public int compare(PhieuNhap pn1, PhieuNhap pn2) {
                return pn1.getMaNhap().compareTo(pn2.getMaNhap());
            }
        });
    }
    /**
     * @param args the command line arguments
     */
    private void showData(List<PhieuNhap> list, DefaultTableModel model){
        try{
            model.setRowCount(0);
            for(PhieuNhap pn : list){
                model.addRow(new Object[]
                {
                    pn.getMaNhap(),
                    pn.getNgayNhap(),
                    pn.getSoLuongNhap(),
                    pn.getGiaNhap(),
                    pn.getTenNCC(),
                    pn.getTenNV()
                });
            }
        }
        catch(Exception ex){
            System.out.println("Loi la : " + ex.getMessage());
        }
    }
    
    
