    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int indexdDelete = tblPhieuNhap.getSelectedRow();
        List<PhieuNhap> list = controller.readDataFromFile("src/file/pnhap.txt");
        try{
            if(list.size() == 0){
                JOptionPane.showMessageDialog(this, "Bảng trống");
            }
            else if(indexdDelete == -1){
                JOptionPane.showMessageDialog(this, "Chưa chọn phiếu nhập cần xóa", "Lỗi", JOptionPane.ERROR_MESSAGE);
                throw new Exception("Loi chon dong");
            }
            else{
                int choice = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa phiếu nhập này?", "Message", JOptionPane.YES_NO_OPTION);
                if(choice == JOptionPane.NO_OPTION){
                    return;
                }
                else{
                    list.remove(indexdDelete);
                    showData(list, modelPhieuNhap);
                    controller.writeToFile(list, "src/file/pnhap.txt");
                }
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnXoaActionPerformed

