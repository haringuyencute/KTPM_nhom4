    private void btnSuaNhanVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaNhanVienActionPerformed
        int itemChosen = tblNhanVien.getSelectedRow();
        try {
            if (itemChosen == -1) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn nhân viên cần sửa thông tin");
                throw new Exception("Chưa chọn nhân viên");
            } else {
                QLNV_SuaNhanVien suaNhanVien = new QLNV_SuaNhanVien(itemChosen);
                suaNhanVien.setVisible(true);
                this.dispose();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSuaNhanVienActionPerformed

    private void btnXoaNhanVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaNhanVienActionPerformed
        int itemChosen = tblNhanVien.getSelectedRow();
        List <NhanVien> nvList = controller.readDataFromFile("src/file/nhanvien.txt");
        try {
            if(nvList.size() == 0) {
                JOptionPane.showMessageDialog(this, "Không có nhân viên nào trong danh sách");
            } else if(itemChosen == -1) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn nhân viên trước khi xoá");
                throw new Exception("Chưa chọn nhân viên");
            } else {
                int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xoá thông tin nhân viên này không?", "Message", JOptionPane.YES_NO_OPTION);
                if(confirm != JOptionPane.YES_OPTION) return;
                else {
                    nvList.remove(itemChosen);
                    showData(nvList, modelNhanVien);
                    controller.writeToFile(nvList, "src/file/nhanvien.txt");
                }
            }
        } catch(Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnXoaNhanVienActionPerformed

