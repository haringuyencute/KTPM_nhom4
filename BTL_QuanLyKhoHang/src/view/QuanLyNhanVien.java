    private void showData(List<NhanVien> nvList, DefaultTableModel model) {
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
        try {
            model.setRowCount(0);
            for (NhanVien nv : nvList) {
                model.addRow(new Object[] 
                {
                    nv.getMaNV(),
                    nv.getHoTen(),
                    nv.getSoDienThoai(),
                    nv.getDiaChi(),
                    dateFormat.format(nv.getNgayVaoLam())
                });
            }
        } catch(Exception ex) {
            System.out.println("Error: " + ex.getMessage());
        }
    }
    
