    private void btn_XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XoaActionPerformed
        int indexremove = tablepx.getSelectedRow();
        List<PhieuXuat> listpx = controller.readDataFromFile("src/file/phieuxuat.txt");
        try{
            if(listpx.size() == 0){
                JOptionPane.showMessageDialog(this, "Chưa có phiếu xuất để xóa!");
            }
            else if(indexremove == -1 ){
                JOptionPane.showMessageDialog(this, "Hãy chọn 1 phiếu để xóa!");
            }
            else{
                int choice = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa phiếu xuất này","Message",JOptionPane.YES_NO_OPTION);
                if(choice == JOptionPane.YES_OPTION ){
                    listpx.remove(indexremove);
                    showData(listpx, modelPX);
                    controller.writeToFile(listpx, "src/file/phieuxuat.txt");
                }
                else{
                    return;
                }
            }
        }
        catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btn_XoaActionPerformed

