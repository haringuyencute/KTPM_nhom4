    private void showData(List<PhieuXuat> list, DefaultTableModel model){
        try{
            model.setRowCount(0);
            for(PhieuXuat px : list){
                model.addRow(new Object[]
                {
                    px.getMaPhieuXuat(),
                    px.getNgayXuat(),
                    px.getSoLuongXuat(),
                    px.getGiaBan(),
                    px.getTenKhachHang(),
                    px.getTenNV(),
                });
            }
        }
        catch(Exception ex){
            System.out.println("Loi la : " + ex.getMessage());
        }
    }
    
    private void btn_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemActionPerformed
        String mapx = txtMaPX.getText();
        String ngayxuat = txtNgayXuat.getText();
        String soluong = txtSoLuongXuat.getText();
        String giaban = txtGiaBan.getText();
        String tennv = txtTenNhanVien.getText();
        String tenKH = txtTenKhachHang.getText();
        List<PhieuXuat> listPX = controller.readDataFromFile("src/file/phieuxuat.txt");
        try{
            if(mapx.isEmpty()){
                JOptionPane.showMessageDialog(this, "Vui lòng nhập mã phiếu xuất!","Lỗi",JOptionPane.OK_OPTION);
                throw new Exception("Lỗi trống mã phiếu xuất!");
            }
            else if(ngayxuat.isEmpty()){
                JOptionPane.showMessageDialog(this, "Không được để trống ngày xuất","Lỗi",JOptionPane.OK_OPTION);
                throw new Exception("Lỗi trống ngày xuất!");
            }
            else if(ngayxuat.isEmpty()){
                JOptionPane.showMessageDialog(this, "Không được để trống ngày xuất","Lỗi",JOptionPane.OK_OPTION);
                throw new Exception("Lỗi trống ngày xuất!");
            }
            else if(soluong.isEmpty()){
                JOptionPane.showMessageDialog(this, "Không được để trống số lượng","Lỗi",JOptionPane.OK_OPTION);
                throw new Exception("Lỗi trống số lượng!");
            }
            else if(giaban.isEmpty()){
                JOptionPane.showMessageDialog(this, "Không được để trống giá bán","Lỗi",JOptionPane.OK_OPTION);
                throw new Exception("Lỗi trống giá bán!");
            }
            else if(tenKH.isEmpty()){
                JOptionPane.showMessageDialog(this, "Không được để trống tên khách hàng","Lỗi",JOptionPane.OK_OPTION);
                throw new Exception("Lỗi trống tên khách hàng!");
            }
            else if(tennv.isEmpty()){
                JOptionPane.showMessageDialog(this, "Không được để trống tên nhân viên","Lỗi",JOptionPane.OK_OPTION);
                throw new Exception("Lỗi trống nhân viên!");
            }
            else if(!checkma(mapx)){
                JOptionPane.showMessageDialog(this, "Mã phiếu đã tồn tại, hãy kiểm tra lại mã phiếu xuất!");
                throw  new Exception("Lỗi trùng mã phiếu xuất!");
            }
            else{
                PhieuXuat PX = new PhieuXuat(mapx, ngayxuat, soluong, tenKH, tennv, giaban);
                listPX.add(PX);
                controller.writeToFile(listPX, "src/file/phieuxuat.txt");
                new QuanLyPhieuXuat().setVisible(true);
                this.dispose();
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_ThemActionPerformed
    public boolean checkma(String mapx){
        List<PhieuXuat> listPX= controller.readDataFromFile("src/file/phieuxuat.txt");
        for(PhieuXuat px: listPX){
            if(mapx.equals(px.getMaPhieuXuat())){
                return false;
            }
        }
        return true;
    }
